#   La programacion Orientada a Objectos (POO) es un paradigma de programacion que utiliza
#   objetos y que representa cosas o conceptos del mundo real y sus interacciones para modelar
#   y resolver  problemas de la vida real. Los objetos son instancias de clases, que representan
#   entidades con propiedades (atributos) y acciones (metodos) especificas. La POO se centra
#   en el encapsulamiento, la herencia y el polimorfismo, que facilitan la reutilizacion del codigo
#   la abstraccion y la modularidad.